#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum TokenKind {
	Ampersand,
	AndKeyword,
	Asterisk,
	BangEqual,
	BoolKeyword,
	BreakKeyword,
	Colon,
	Comma,
	ConstKeyword,
	ContinueKeyword,
	Dash,
	DecKeyword,
	DecimalLiteral,
	Dot,
	ElseKeyword,
	EndOfFile,
	Equal,
	EqualEqual,
	FalseKeyword,
	FnKeyword,
	ForKeyword,
	ForwardSlash,
	GreaterThan,
	GreaterThanOrEqual,
	Identifier,
	IfKeyword,
	InKeyword,
	IntKeyword,
	InterpolatedStringEnd,
	InterpolatedStringMiddle,
	InterpolatedStringStart,
	IntegerLiteral,
	LeftBrace,
	LeftBracket,
	LeftParenthesis,
	LessThan,
	LessThanOrEqual,
	MinusEqual,
	MultiplyEqual,
	NotKeyword,
	ObjKeyword,
	OrKeyword,
	Percent,
	PercentEqual,
	Plus,
	PlusEqual,
	ReturnKeyword,
	RightBrace,
	RightBracket,
	RightParenthesis,
	Semicolon,
	SlashEqual,
	StringLiteral,
	TextKeyword,
	TrueKeyword,
	VarKeyword,
	VoidKeyword,
	WhileKeyword,
	WithKeyword,
	XorKeyword,
}

#[derive(Clone, Debug, Eq, PartialEq)]
pub struct Token {
	pub end: usize,
	pub kind: TokenKind,
	pub lexeme: String,
	pub start: usize,
}
