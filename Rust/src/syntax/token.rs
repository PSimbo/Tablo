#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum TokenKind {
	AndKeyword,
	Asterisk,
	BangEqual,
	BoolKeyword,
	Colon,
	ConstKeyword,
	Dash,
	DecKeyword,
	DecimalLiteral,
	EndOfFile,
	Equal,
	EqualEqual,
	FalseKeyword,
	ForwardSlash,
	GreaterThan,
	GreaterThanOrEqual,
	Identifier,
	IntKeyword,
	InterpolatedStringEnd,
	InterpolatedStringMiddle,
	InterpolatedStringStart,
	IntegerLiteral,
	LeftBrace,
	LeftParenthesis,
	LessThan,
	LessThanOrEqual,
	MinusEqual,
	MultiplyEqual,
	NotKeyword,
	OrKeyword,
	Percent,
	PercentEqual,
	Plus,
	PlusEqual,
	RightBrace,
	RightParenthesis,
	Semicolon,
	SlashEqual,
	StringLiteral,
	TextKeyword,
	TrueKeyword,
	VarKeyword,
	XorKeyword,
}

#[derive(Clone, Debug, Eq, PartialEq)]
pub struct Token {
	pub end: usize,
	pub kind: TokenKind,
	pub lexeme: String,
	pub start: usize,
}
