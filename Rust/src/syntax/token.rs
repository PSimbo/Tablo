#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum TokenKind {
	AndKeyword,
	Asterisk,
	BangEqual,
	BoolKeyword,
	Colon,
	Dash,
	DecKeyword,
	DecimalLiteral,
	EndOfFile,
	Equal,
	EqualEqual,
	FalseKeyword,
	ForwardSlash,
	GreaterThan,
	GreaterThanOrEqual,
	Identifier,
	IntKeyword,
	IntegerLiteral,
	LeftParenthesis,
	LessThan,
	LessThanOrEqual,
	NotKeyword,
	OrKeyword,
	Percent,
	Plus,
	RightParenthesis,
	TrueKeyword,
	VarKeyword,
	XorKeyword,
}

#[derive(Clone, Debug, Eq, PartialEq)]
pub struct Token {
	pub end: usize,
	pub kind: TokenKind,
	pub lexeme: String,
	pub start: usize,
}
